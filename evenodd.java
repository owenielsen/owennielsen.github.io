public class evenodd {
    public static void main(String[] args){
        // Loop from 1 to 100
        for (int i = 1; i <= 100; i++) {
            // Check if the number is even or odd 
            if (i % 2 == 0) {
                System.out.println(i + " is even");
            } else {
                System.out.println(i + " is odd");
            }
        }
    }
}